export function getService(slug: string): Service | undefined {
  return services.find((s) => s.slug === slug);
}

export function getServiceByHubSlug(hubSlug: string): Service | undefined {
  return services.find((s) => s.hubSlug === hubSlug);
}

export function getServiceByCityPrefix(
  prefix: string,
): Service | undefined {
  return services.find((s) => s.citySlugPrefix === prefix);
}

export function getSubService(
  serviceSlug: string,
  subSlug: string,
): { service: Service; subService: SubService } | undefined {
  const service = getService(serviceSlug);
  if (!service) return undefined;
  const subService = service.subServices.find((sub) => sub.slug === subSlug);
  if (!subService) return undefined;
  return { service, subService };
}
